import Img from '@/components/Img'
import React from 'react';
import Image from "next/image";
import {
  motion,
  useScroll,
  useSpring,
  useTransform,
  MotionValue,
} from "framer-motion";
import img from "../public/carImg.jpg";
import Parallax from '@/components/Parallax'
